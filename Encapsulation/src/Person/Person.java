package Person;

class Person {
    private String name;
    private int age;
    public void setName(String n) {
        name = n;
    }
    public String getName() {
        return name;
    }
    public void setAge(int a) {  
            age = a;
    }
    public int getAge() {
        return age;
    }
}   //this program shows encapsulation ,attributes name and age are encapsulated.

